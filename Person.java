public class Person{
  String name;
  String nickname;
  int age;
  String favoriteShow;
  String favoriteBand;


 public Person(String givenName, String givenNickname, int givenAge, String givenFavoriteShow, String givenFavoriteBand){
   name = givenName;
   nickname = givenNickname;
   age = givenAge;
   favoriteShow = givenFavoriteShow;
   favoriteBand = givenFavoriteBand;
 }

}