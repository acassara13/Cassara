public class PersonLong{
  String name;
  String nickname;
  int age;
  String favoriteShow;
  String favoriteBand;
  boolean isProgrammer;
  String state;
  String hairColor;
  String motto;
  

 public PersonLong(String givenName, String givenNickname, int givenAge, String givenFavoriteShow, String givenFavoriteBand, boolean givenProgrammer, String givenState, String givenHairColor, String givenMotto){
   name = givenName;
   nickname = givenNickname;
   age = givenAge;
   favoriteShow = givenFavoriteShow;
   favoriteBand = givenFavoriteBand;
   isProgrammer = givenProgrammer;
   state = givenState;
   hairColor = givenHairColor;
   motto = givenMotto;
 }

}